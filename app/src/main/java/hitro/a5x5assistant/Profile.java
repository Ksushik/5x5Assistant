public class Profile {

    String name, bodyweight, squat, bench, row, ohp, dl;

    public Profile() {

    }


    public Profile(String name, String bodyweight, String squat, String bench, String row, String ohp, String dl) {
        this.name = name;
        this.bodyweight = bodyweight;
        this.squat = squat;
        this.bench = bench;
        this.row = row;
        this.ohp = ohp;
        this.dl = dl;
    }
}
